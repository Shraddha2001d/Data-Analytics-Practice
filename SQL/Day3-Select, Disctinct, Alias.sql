/*DISTINCT CLAUSE/KEYWORD:
"IT IS USED TO REMOVE DUPLICATE/REPETED VALUE FROM RESULT TABLE"
->ALWAYS DISTINCT CLAUSE HAS TO BE USED AS FIRST ARGUMENT IN
SELECT CLAUSE.
->FOR DISTINCT CLAUSE WE CAN PASS MULTIPLE COLUMNS NAMES AS ARGUMENTS
IT WILL CHECK FOR COMBINATION OF COLUMNS IN WHICH RECORD IS REPETED".

*/
SELECT DISTINCT DEPTNO
FROM EMP;

SELECT DISTINCT JOB
FROM EMP;
SELECT SAL,DEPTNO
FROM EMP;

SELECT DISTINCT SAL,DEPTNO
FROM EMP;


