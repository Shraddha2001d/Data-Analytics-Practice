/*Overview of SQL Statements:
1)Data Definition Language(DDL):
"it is used constuct the object in a database &delas
with structure of object".
->it has 5 statements.
1)CREATE
2)RENAME
3)ALTER
4)TRUNCATE
5)DROP
2)Data Manipulation Language(DML):
"IT IS USED TO MANIPULATE THE OBJECT BY PERFORMING 
1)INSERT
2)UPDATE
3)DELETE 
OPERATIONS.
3)Transaction Control language(TCL:
"IT IS USED TO CONTROL FLOW OF TRANSACTIONS IN  DATABASE"
->IT HAS 3 STATEMENTS
1)COMMIT
2)ROLLBACK
3)SAVEPOINT
4)Data Control Language(DCL):
"IT IS USED TO CONTROL FLOW OF DATA IN DATABASE"
->IT HAS 2 STATEMENTS
1)GRANT
2)REVOKE
5)Data Query language(DQL)
"IT IS PROCESS RETRIVE THE DATA FROM DATABASE".
->IT HAS 4 STATEMENTS
1)SELECT:
"IT IS PROCESS OF RETRIVING DATA FROM THE 
TABLE &DISPLAY IT".
2)PROJECTION
"IT IS PROCESS OF RETRIVING DATA BY SELECTING ONLY
COLUMNS".
3)SELECTION
"IT IS PROCESS OF RETRIVING DATA BY SELECTING BOTH
ROWS &COLUMNS IS KNOWN AS SELECTION.
4)JOINS:
"IT IS PROCESS OF RETIVING DATA FROM MULTIPLE TABLES
SIMULTANESOULY IS KNOWN AS JOINS".

