SELECT CLAUSE.
->FOR DISTINCT CLAUSE WE CAN PASS MULTIPLE COLUMNS NAMES AS ARGUMENTS
IT WILL CHECK FOR COMBINATION OF COLUMNS IN WHICH RECORD IS REPETED".

*/
